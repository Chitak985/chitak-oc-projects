----- CLEAR SPACE -----
function clear3xn(n)
  equipSafe("Vajra")
  for i = 1, n - 1, 1 do
    clearSquare3()
    swingU()
    u()
  end
  clearSquare3()
  for i = 1, n - 1, 1 do
    d()
  end
end
function clearSquare3()
  for i = 1, 3, 1 do
    swing()
    f()
  end
  for i = 1, 3, 1 do
    tr()
    swing()
    ta()
    swing()
    tr()
    b()
  end
end
